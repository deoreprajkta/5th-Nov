package Javabasic;

public class DecreIncre {

	public static void main(String[] args) {
		int a = 20;
		System.out.println(a);
		System.out.println(a++);
		System.out.println(--a);
		
		int b = 25;
		System.out.println(b--);
		System.out.println(b);
		System.out.println(--b);
		

	}

}
