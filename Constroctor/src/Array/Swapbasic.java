package Array;

public class Swapbasic {

	public static void main(String[] args) {
		
		int a = 78;
		int b = 65;
		
		System.out.println(a);
		System.out.println(b);
		
		
		System.out.println("Integate swappint");
		int c ;
		c = a;
		a = b;
		b= c;

		System.out.println(a); //65
		System.out.println(b); //78
		

	}

}
