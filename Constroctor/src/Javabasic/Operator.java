package Javabasic;

public class Operator {

	public static void main(String[] args) {
		int a =10,
		    b =15,
		    c = 30;
		    
		    System.out.println("a+b=" +(a+b));
		    
		    System.out.println("a-b=" +(a-b));
		    
		    System.out.println("a/b="+(a/b));
		    System.out.println("a*b="+(a*b));
		    System.out.println("a%b="+(a%b));
		    System.out.println("a*b*c="+(a*b*c));
		    System.out.println("a-b*c="+(a-b*c));
		    System.out.println ("a+a-b="+(a+a-b));
		    System.out.println("c+a-b*a="+(c+a-b*a));
	}

}
