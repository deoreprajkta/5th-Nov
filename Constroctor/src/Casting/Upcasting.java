package Casting;

public class Upcasting {
	
	public void Demo() {
		System.out.println("Non static super class method");
	
	}
	public void Run() {
		System.out.println("Super class run method is running");
		
	}
	public static void Staticcasting() {
		System.out.println("Super class static method");
	}
	

}
