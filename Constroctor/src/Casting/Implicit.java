package Casting;

public class Implicit {
	
	public static void main(String[]args) {
		byte a = 127;
		System.out.println(a);
		
		int b = a;
		System.out.println(b);
		
		double c = b;
		System.out.println(c);
		long d = (long)c;
		System.out.println(d);
		
		
		
		
		
		
		
		
		
	}

}
